""""
								Подъезды и этажи 🏢
В девятиэтажном доме в каждом подъезде на каждом этаже расположено по 4 квартиры.
То есть в одном подъезде всего 36 квартир.
Напишите программу, которая по номеру квартиры определяет, в каком подъезде она находится.

    Входные данные: Целое число (номер квартиры, начинается с 1).
    Выходные данные: Одно число (номер подъезда).

"""

apartments_number = int(input("Назовите пожалуйста № квартиры"))
entrance_number = (apartments_number - 1) // 36 + 1
flat_in_entrance = (apartments_number - 1) % 36 + 1
floor_number = (flat_in_entrance - 1) // 4 + 1

print("Квартира №", apartments_number, "находится в", entrance_number, "подъезде", "на", floor_number, "этаже.")



