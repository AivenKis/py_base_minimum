"""""
                Задача 2
                Задание:

Определите функцию sum_if_less_than_fifty, принимающую два параметра:
    Имя        |        Тип           |          Пример входа
num_one              int                           20
num_two        |     Int              |            25

Функция должна возвращать:
     - сумму двух чисел, если эта сумма меньше 50;
     - None, если сумма двух чисел больше или равна 50.
"""

"""""
                            Подробное объяснение кода
    Структура функции проста и логична, она состоит из трех этапов: вычисление, проверка, возврат результата.

1.	Определение функции:
o	def sum_if_less_than_fifty(num_one, num_two): — мы создаем функцию, которая ожидает два аргумента (num_one и num_two). Согласно условию, это целые числа (int).

2.	Вычисление суммы:
o	total_sum = num_one + num_two — мы складываем переданные числа и сохраняем результат во временную переменную total_sum. Это делает код более читаемым и позволяет избежать повторного сложения внутри условий.

3.	Условный оператор (if / else):
o	if total_sum < 50: — проверяем, строго ли меньше 50 наша сумма. 
	Если Да (True): срабатывает return total_sum. Функция немедленно возвращает число и завершает работу.
o	else: — этот блок срабатывает во всех остальных случаях (то есть, если сумма равна 50 или больше 50). 

В этом случае выполняется return None
"""
def sum_if_less_than_fifty(num_one, num_two):
    # 1. Считаем сумму двух аргументов
    total_sum = num_one + num_two

    # 2. Проверяем условие
    if total_sum < 50:
        return total_sum
    else:
        return None


# --- Тестирование ---
# Случай 1: Сумма меньше 50 (20 + 25 = 45)
print(sum_if_less_than_fifty(25, 20))  # Вывод: 45

# Случай 2: Сумма больше или равна 50 (30 + 20 = 50)
print(sum_if_less_than_fifty(30, 20))  # Вывод: None

# Случай 3: Сумма значительно больше 50 (50 + 50 = 100)
print(sum_if_less_than_fifty(50, 50))  # Вывод: None


