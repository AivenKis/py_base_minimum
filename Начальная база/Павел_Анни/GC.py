# Пример № 5
def process_data(items):
    print(len(items))                       # тут адрес items лежит в стеке, а ссылка на список [1, 2, 3] лежит в куче, и items указывает на этот список

data_list = [1, 2, 3]                       # тут адрес data_list лежит в стеке, а список [1, 2, 3] лежит в куче, и data_list указывает на этот список
process_data(data_list)                     # тут мы вызываем функцию process_data, которая принимает ссылку на список [1, 2, 3], который лежит в куче, и выводит его длину, после завершения функции ссылка на список остается в куче, так как она все еще существует в переменной data_list


