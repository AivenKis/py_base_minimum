# Напишите функцию modify_list(l), которая принимает на вход список целых чисел, удаляет из него все нечётные значения, а чётные нацело делит на два.

def modify_list():
	while True:
		try:
			# Получение ввода от пользователя
			user_input = input("Введите список целых чисел через пробел: ")
			
			# Попытка преобразовать все элементы в целые числа
			numbers = []
			for item in user_input.split():
				numbers.append(int(item))
			
			# Обработка списка: удаление нечетных, деление четных на 2
			result = []
			for num in numbers:
				if num % 2 == 0 and num != 0:  # Если число четное и не равно нулю
					result.append(num // 2)
			
			return result
		
		except ValueError:
			print(
				"Ошибка: Вы ввели некорректные данные. Пожалуйста, введите только целые числа, разделенные пробелами.")
			continue  # Предложить пользователю ввести данные заново


# Запуск функции и вывод результата
modified_list = modify_list()
print("Результат:", modified_list)


