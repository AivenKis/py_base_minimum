def countdown(i):  # Объявление функции, которая принимает аргумент i
    print(i)  # Сразу выводим текущее число на экран
    if i <= 1:  # БАЗОВЫЙ СЛУЧАЙ (Условие выхода)
        return  # Если i равно 1 или меньше, мы прекращаем выполнение функции
    else:  # РЕКУРСИВНЫЙ СЛУЧАЙ (Шаг продолжения)
        countdown(i - 1)  # Функция вызывает сама себя, но уменьшает число на 1


countdown(2)

