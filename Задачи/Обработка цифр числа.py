#                               Обработка цифр числа
num = 795

# Шаг 1 - достаём последнюю цифру (Пинцетом):
print(num % 10) # 79 * 10 = 790 получим остаток 5 (вытащили пятёрку)

# Шаг 2 - топором укорачиваем число:
print(num // 10) # дробная часть отваливается, получается число 79 с которым работаем дальше

# Шаг 3 - опять пинцетом из нового числа достаём последнюю цифру
new_num = 79
print(new_num % 10) # в остатке получим 9, это число достали путём: 10 * 7 = 70 а в остатке 9

# Шаг 4 - опять топор берётся за дело, чтобы отсечь дробную часть
print(new_num // 10) # получаем 7 (девятка отсекается)

