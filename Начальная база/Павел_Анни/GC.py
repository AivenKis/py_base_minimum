def start():
    message = "Привет!"  # адрес message лежит в стеке, строка "Привет!" лежит в куче, message указывает на эту строку
    print(message)       # записали сообщение в память (кэш), которое взяли в куче

start()  				# Функция завершилась, ссылки на объект нет, самого объекта соотвественно тоже





