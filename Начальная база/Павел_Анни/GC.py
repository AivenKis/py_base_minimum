current_status = get_status()             # адрес current_status лежит в стеке, словарь {"code": 200, "text": "OK"} лежит в куче, current_status указывает на этот словарь


# Пример № 3
logs = []                                  # Глобальный список

def add_log(text):
    entry = {"msg": text}               # адрес entry лежит в стеке, словарь {"msg": text} лежит в куче, entry указывает на этот словарь
    logs.append(entry)                  # logs - глобальный список, который лежит в куче, и мы добавляем в него ссылку на словарь {"msg": text}, который тоже лежит в куче

add_log("User logged in")              # тут мы вызываем функцию add_log, которая создает словарь {"msg": "User logged in"} в куче и добавляет ссылку на этот словарь в глобальный список logs,
                                      # который тоже лежит в куче, # словарь удалён не будет




